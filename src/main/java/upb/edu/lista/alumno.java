public class alumno {
    int edad;
    String nombre;
    public alumno(int edad, String nombre){
        this.edad=edad;
        this.nombre=nombre;
    }

    public int getEdad() {
        return edad;
    }
 

    public String getNombre() {
        return nombre;
    }

     
    
}
